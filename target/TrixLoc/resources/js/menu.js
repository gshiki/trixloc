/**
 * Inicializa os links do menu com o evento de abrir o popup.
 */
function initMenu() {
	var $elementsMenuListItems = $('li[open-popup]');
	
	if (exists($elementsMenuListItems)) {
		for(var index = 0; index < $elementsMenuListItems.length; index++) {
			var $elementMenuItem = $($elementsMenuListItems[index]);
			
			if (exists($elementMenuItem)) {
				$elementMenuItem.click( { element : $elementMenuItem }, handleItemClick );
			}
		}
	}
}

/**
 * Abre o popup e cancela o evento padrao dos links.
 * @param event
 */
function handleItemClick(event) {
	showMapPopup(event);
	return false;
}
